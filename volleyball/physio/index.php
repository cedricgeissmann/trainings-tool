<div class="row">

<img class="col-md-3" src="img/aussenband2.png"></img>
<div  class="col-md-9">
<ol>
<li>Flach auf den Boden liegen.</li>
<li>Beine anwinkeln und Hüfte in die höhe drücken.</li>
<li>Spannung im ganzen Körper halten.</li>
<li>Wenn keine Schmerzen im Knie zu spüren sind, das gesunde Bein anheben, und nur noch auf dem verletzten stehen.</li>
</ol>

<p>2 mal täglich ca. 30 bis 60 Sekunden.</p>
<p>Wenn das gut geht, Fuss auf eine unstabile oder weiche Unterlage legen. Zum Beispiel eine Matratze oder wenn es ganz gut geht auf einen Ball.</p>
