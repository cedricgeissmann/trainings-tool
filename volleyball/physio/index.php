<h2>Wichtig</h2>
<p>Alle Übungen sofort abbrechen wenn Schmerzen auftretten. Dann dem Bein eine Pause gönnen, und am nächten Tag, vorsichtig an diese Belastung herantasten. Genug Zeit für die Übungen nehmen, und immer alle sehr Gewissenhaft und korrekt ausführen.</p>
<p>Solange nicht völlig stabil, Fehlbelastungen vermeiden. 2 nis 3 mal täglich gut mit Voltaren Dolo einmassieren.</p>
</section>

